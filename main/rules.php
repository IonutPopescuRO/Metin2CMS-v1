	<div class="alert alert-info" role="alert">Reguli pentru utilizare</div>
	* Niciun membru al jocului nu poate invoca, drept scuză pentru
	încălcarea acestui Regulament, faptul că nu ştia de existenţa lui.<br>
	* Echipa are dreptul să modifice oricând prevederile Regulamentului;
	recomandarea noastră ar fi să citiţi periodic prezentul Regulament,
	pentru a cunoaşte eventualele actualizări oficiale din joc.<br> *
	Operatorii de joc au obligaţia să respecte în mod comun deciziile
	conforme cu prezentul regulament.<br> * Suntem unicii în măsură să
	judece ce anume reprezintă o încălcare a regulamentului. Neconformarea
	cu termenii şi condiţiile de mai jos constituie motiv pentru
	suspendarea sau dezactivarea contului.<br>
	<br> <a><b>1.Numele de utilizator:</b></a><br> - Numele utilizatorilor
	nu trebuie să fie ofensatore, neadecvate, rasiste, extremiste, vulgare
	sau care fac reclamă altor servere.<br> - Jucătorii ce vor folosi nume
	asemănătoare echipei sau derivate a acestora vor primi ban fără a fi
	atenţionaţi.<br> - Nu aveţi voie să vă face caractere de genul "
	IIIIIIIIIIIIIIIiiiiIIIIIIIII " sau "11232155689",dacă veţi fi prinşi cu
	asemenea caractere în joc veţi fi blocați permament.<br> - De asemenea
	nu aveți voie să folosiți nume identice cu ale celorlalți jucători și
	să cereți iteme în numele lui (exemplu: GeorgeLiLi - GeorgeLiLl) - cei
	ce se vor folosii de acest lucru, vor fi blocați permanent.<br>
	<br> <a><b>2.Denumirile breslelor,magazinelor:</b></a><br> - Nu trebuie
	să conţină de asemenea nume ca la punctul 1, pedeapsa va fi aceeași,
	ban permanent sau un avertisment.<br> - Magazinele care conțin anunțuri
	ilegale(schimburi între servere), prima dată vor primi blocarea
	contului 3 zile, ulterior la a 2-a abatere, vor fi blocați permanent.<br>
	<br> <a><b>3.Comportamentul în joc:</b></a><br> - Este interzis a se
	transmite mesaj privat in joc, sau prin orice sistem de mesagerie, care
	conține insulte, jigniri sau acuzații aduse echipei, de asemenea
	jignirile, aluziile de natură jignitoare/ofensatoare aduse altor
	jucători sunt strict interzise. Pentru limbaj, sancţiunea constă în
	blocare chatului în funcție de gravitate.<br> - Limbajul public
	indecent între jucători ce susţin a fi prieteni, se sancţionează cu
	blocarea chatului. Pentru discuții într-un anumit grup, există alte
	metode decât cel public.<br> - Este interzis, şantajul unui jucător
	pentru obţinerea bunurilor sau furtul de conturi.<br> - Este interzisă
	utilizarea modului de a ieşi în evidenţa prin SPAM (maxim 3 mesaje la
	rând).<br> - Rostirea numelor altor servere prin comercializare,
	promovare sau prin discuţii deschise, se sancţionează cu blocarea
	contului permanent sau temporar.<br> - Se sancţionează cu blocare chat
	timp de 1 oră, răspunsuri eronate, fără apartenenţa temei discutate, la
	întrebările jucătorilor noi participanţi la joc. Dacă nu aveți starea
	necesară să le raspundeți corect, nu mai raspundeți, chiar dacă aceștia
	insistă, spuneți-le că nu stiți.<br> - Sancţiunile pentru limbaj &amp;
	comportament verbal, nu pot avea loc obligatoriu imediat după fapta
	comisă. Este obligaţia Jucătorului implicat să păstreze dovezile.<br> -
	Nu se permit înjurături sau ostilităţi. Cine nu se va comporta
	civilizat, va trebui să se aştepte la consecinţe. Acest lucru este
	valabil pentru toţi utilizatorii. Gradul consecinţelor depinde de tipul
	erorii. Sancţiunea consta în blocarea chatului sau blocarea contului
	jucătorului. În caz că cineva v-a jignit/înjurat, postați pe forum în
	secțiunea reclamații.<br> - Pe perioada evenimentelor, jucătorii sunt
	obligaţi să păstreze liniştea &amp; evitarea emiterii de mesaje
	abundente de tip spam. În caz ca aceștia vor deranja evenimentele,
	contul lor v-a fi blocat.<br> - Cel mai bine este să așteptați
	indicațiile GM-ului și totul v-a decurge bine și repede.<br>
	<br> - Limba oficială folosită în joc este limba română şi engleză.<br>
	- Nu aveţi voie să deschideţi cufere şi/sau aruncaţi pe jos iteme sau
	să chemați Șefi sau Pietre Metin ca să faceţi lag în map 1, riscați
	blocare permanentă sau pe mai multe zile, în funcție de gravitate.<br>
	- Nu aveţi voie să vă schimbaţi regatele ca să participaţi de mai multe
	ori la eventuri,să fiţi şi la eventul de pe Galben,albaştrii,roşii. -
	Riscați blocare permanentă.<br> - Orice jucător care va interveni peste
	un grup care încearcă să omoare Azrael va primi blocarea contului 3
	zile, reclamațiile trebuie făcute clare, cum a intrat peste voi faceți
	poză, dacă a rămas și vă omoară sau se bagă peste Azrael, faceți print
	și așa dovediți, de asemenea o convorbire între voi, în care ați
	încercat să-i spuneți să iasă și că nu are voie în Catacombe.<br> -
	Dacă omorâţi un jucător şi apoi îl blocaţi cu trade [ negociază ] şi
	sunteţi raportaţi pe forum, primiţi blocarea contului de la 3 la 7
	zile.<br>
	<br> <a><b>4.Comportamentul cu echipa:</b></a><br> - GM este
	prescurtarea de la Game Master. GM nu îți vor da Item-uri, Yang sau
	orice altceva. GM sunt aici să te ajute și să aibă grijă ca jocul și
	chat-ul să se desfășoare în limitele regulamentelor. Va trebui să te
	supui îndrumărilor date de GM. Contul tău din joc poate fi blocat dacă
	nu te supui acestor îndrumări. GM te pot bloca temporar sau permanent
	și pot lua orice hotărâre este nevoie pentru a menține o bună
	desfășurare a jocului.<br> - Postarea discuţiilor purtate cu membrii
	echipei de suport (ticketele,livechat) fără acordul scris al
	adminului/operatorului respectiv, se sancţionează direct cu blocarea
	contului.<br> - Acuzaţiile nefondate aduse echipei (atât pe forum cât
	şi joc), pot duce la blocarea permanentă a respectivilor jucători.
	Orice aţi scrie poate fi folosit ca mijloc de probă împotriva
	dumneavoastră în cadrul unei contestaţii. Noi nu vom accepta dacă mai
	târziu veţi spune că a fost o glumă.<br> - Nu este permis limbajul
	familiar/ofensator purtat cu membrii echipei.<br> - Membrilor de
	echipă, nu le este permisă folosirea unui limbaj familiar/colocvial şi
	indecent.<br> - Se sancţionează cu avertisment comunicarea prin Shop
	(Mănunchi) privind contestaţia blocării chatului. Orice jucător are
	dreptul la contestaţie, excepţie făcând cel precizat.<br> - Nu se vor
	sancţiona jucători ce au purtat limbaj indecent contra echipei sau
	altor jucători, prin metode de comunicare externe precum: Skype
	(conferinţe,mesaj), Y!Messenger, Facebook. Totuși aveți grijă, dacă
	abuzați de acest lucru și ne insultați în repetate rânduri, putem trece
	la fapte.<br> - Urgentarea echipei. Unele răspunsuri necesită ceva
	timp. Ne bucurăm de o participare intensă dar totuşi nu putem garanta
	răspunsuri prompte din mesageria privată(PM). Grăbirea echipei nu ajută
	la nimic, riscând chiar blocarea chatului. Va rugăm să conştientizaţi
	faptul că nu sunteţi singurii jucători ce solicită informaţii şi
	sprijin. De asemenea nu orice mesaj merită un răspuns.<br> - Jucătorii
	NU AU DREPTUL să testeze cunoştiinţele unui Game Master, sau să-i tragă
	la răspundere! Asta se consideră "sfidarea unui membru staff" şi duce
	la mute ! [ 4 - 8 ore ] în funcţie de GM şi de gravitate. În cel mai
	rău caz ban 3 zile!<br> - De asemenea când apelaţi la un GM nu folosiţi
	limbaj gen "wă,bă,alo,wei,etc" .După ce aţi salutat GM-ul puneţi direct
	întrebarea,nu mai aşteptaţi să vă răspundă la salut.<br> - Jucatorii
	sunt obligați să răspundă mesajelor private din joc, primite de la GM.
	Dacă după o anumită perioadă de timp jucătorul nu răspunde, se
	consideră că folosește un soft ilegal doar pentru a îi ține caracterul
	în joc fără ca utilizatorul să joace și se pedepsește cu blocarea
	permanentă a contului.<br> - De asemenea ceilalți membri ai echipei
	trebuie tratați cu același respect și recunoaștere, ei sunt aleși în
	echipă doar pentru a vă face un joc mai plăcut, în caz că aceștia se
	abat de la regulament pot fi concediați, dacă ați fost martori sau
	membri echipei au făcut abatere de la regulament, sunteți rugați să îi
	reclamați la secțiunea Reclamații Echipă.<br>
	<br> <a><b>5.Războaiele între breasle:</b></a><br> - Nu aveți voie să
	vă transformaţi la un război între bresle - blocarea contului
	permament.<br> - Nu aveți voie să organizați blaturi între breasle(
	război fantomă), adică cealaltă echipă să piardă și voi să câștigați-
	blocarea contului 7 zile, la încercări repetate pe ambele conturi cu
	pricina(liderii), primiți blocarea contului permanent.<br> - Nu aveți
	voie să fugiți la un război pe motive că nu aveți jucători, etc.. -
	avertisment/blocarea contului 3 zile.<br> - Nu aveți voie să stați
	blocati în pereți, departe de atacul inamicului la război - blocarea
	contului 7 zile/permanent.<br>
	<br> <a><b>6.Greşeală de program (bug) &amp; abuzuri:</b></a><br> -
	Fiecare jucător este obligat să anunţe imediat descoperirea greşelilor
	de pe server sau comunitate.<br> - Exploatarea acestor greșeli nu duce
	decât la blocarea permanentă a contului.<br> - Dacă observi că primeşti
	unităţi sau bunuri mai multe decât în mod normal, nu ai voie să
	exploatezi faptul acesta şi trebuie să anunţi greşeala. Consultaţi-vă
	cu Echipa de pe joc sau postați pe forum în secțiunea Bug-uri - Bugs.
	Dacă vedeți persoane utilizându-se de aceste greșeli, raportați pe
	forum în secțiunea Reclamații Jucători.<br> - Este interzisă
	exploatarea bug-urilor (erorilor) şi/sau a greşelilor în scopuri
	proprii.<br>
	<br> <a><b>7.Hacking, decriptare &amp; trişat (client de joc):</b></a><br>
	- Nu permitem utilizatorilor, în cadrul posibilităţilor tehnice şi
	operaţionale disponibile, participarea la joc şi serviciile oferite, cu
	metode hacking, decriptare client sau alte mijloace de trişare şi abuz.<br>
	- Softuri, configuraţii maliţioase sau decriptări, sunt nepermise şi
	detectabile.<br> - Singurul soft ajutător permis, este switchbot-ul.<br>
	- Singura sancţiune strictă pentru astfel de activitate, este blocarea
	permanentă a conturilor.<br>
	<br> <a><b>8.Comerțul:</b></a><br> - Comerțul de conturi,iteme sau Yang
	între servere/servere private/oficiale este interzis, la prima abatere
	se pedepsește cu blocarea chat-ului pe contul în cauză sau blocarea
	contului 3 zile, în funcție de accesele GM-ului , iar la a doua abatere
	blocarea contului permanent.<br> - În cazul reclamațiilor pe forum, sau
	dacă cineva din echipă consideră că cel ce a încălcat regulamentul
	meriăa direct contul blocat, jucătorul va primi pedeapsa meritată.<br>
	- La schimburile pe server puteți cere ajutorul unui membru al echipei,
	dacă luați decizia să efectuați schimbul individual, în caz că celălalt
	jucător o să vă înșele, nimeni nu vă mai poate ajuta.<br> - Schimburile
	pe server pot fi cont - cont, cont - iteme/yang, iteme/yang -
	iteme/yang.<br> - Schimburile yang/iteme joc pe euro/lei reali sunt
	ilegale, riscați să primiți blocarea chat-ului sau a contului
	permanent.<br>
	<br> <a><b>9.Conturile:</b></a><br> - Este interzisă crearea unui cont
	având că singur scop susţinerea altui jucător (bun gratuit, bani &amp;
	breaslă). Dacă decideţi să părăsiţi jocul, este interzis să continuaţi
	să jucaţi doar pentru susţinerea altui cont. Bun gratuit, înseamnă item
	ce face excepţie de la gameplay.<br> - Blocarea conturilor nu poate
	avea loc obligatoriu imediat după fapta comisă.<br> - Sunt excluse
	orice alte pretenţii ale utilizatorului cu privire la restituirea de
	plăţi efectuate, atât timp cât nu este convenit altfel în aceste
	condiţii. Donatiile nu scutesc conturile de utilizatori de la
	respectarea prezentului regulament şi termeni.<br> - Conturile ce au
	acumulat 4 avertismente emise, sunt blocate permanent.<br> -
	Înstrăinarea datelor referitoare la cont cu terţi (ex. alţi jucători)
	nu este recomandată.<br> - Nu suntem responsabili în niciun fel pentru
	orice daună care rezultă în urma nerespectării acestei restricţii. Vă
	recomandăm să nu faceţi cunoscute nimănui informaţii cu caracter
	personal. Nu putem şi nu vom fi traşi la răspundere pentru acţiunile
	voastre în cazul unui furt al contului sau al altui abuz.<br> - Este de
	datoria ta să te asiguri că datele contului tău sunt în siguranţă.
	Împărtăşind aceste date cu altcineva înseamnă să pierzi credibilitatea
	reclamaţiilor aduse în cazul pierderii contului sau a Item-urilor
	aflate în cont. Echipa nu este responsabilă de conţinutul şi
	identitatea contului.<br> - Înţelegeţi şi sunteţi de acord că sunteţi
	unicul responsabil pentru propriul conţinut şi pentru consecinţele
	neglijenţei asupra datelor. Echipa nu garantează recuperarea
	conţinutului pierdut din urma neglijenţei jucătorilor sau lipsa
	măsurilor preventive de securitate.<br> - Nu vom reacţiona împotriva
	cazurilor de negocieri, împrumuturi de iteme din propria voinţă, sau
	schimburi înşelătoare între servere(ceea ce nu e legal), cu anumite
	pagube drept rezultat şi prejudicii.<br> - Nu acceptaţi fişiere de la
	persoanele necunoscute,este posibil ca acele fişiere să conţină
	viruşi/worms/keyloggere, aveți grijă ce descărcați pe calculator,
	folosiți un antivirus bun, în orice se pot afla programe care va pot
	fura datele personale.<br> - Nu umblați pe diferite site-uri cu tombole
	sau diferite concursuri ale serverului, tot ce facem anuntam pe forum,
	daca folositi acele site-uri in speranta de a castiga ceva, veti avea
	parte doar de un singur lucru, spargerea contului si nimic mai mult.<br>
	- NU ESTE VINA UNUI GM SAU a ADMINISTRATORILOR (GA) ca v-a fost "SPART"
	CONTUL și nici nu putem împiedica acest lucru deoarece în 90% din
	cazuri dumneavoastră oferiți parola la alte persoane ori, luați
	diferite programe sau multe alte lucruri ce conțin keyloggere ascunse
	de pe net.<br> - Un membru din echipă are mereu scrisul galben, iar
	când mesajul apare chat va fi de asemenea tot galben, nu vă luați după
